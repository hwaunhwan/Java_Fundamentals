package datatypes_operators.labs;

/**
 * Please demonstrate the use of all arithmetic operators below. These include:
 *
 * addition, subtraction, multiplication, division and modulus
 *
 */
class ArithmeticOperators {

    public static void main(String[] args) {

        // write your code below
        int add = 1 + 2;
        int sub = 2 - 1;
        int mult = 1 * 2;
        int div = 2 / 1;
        int mod = 10 % 3;

        System.out.println("1 + 2 = " + add);
        System.out.println("2 - 1 = " + sub);
        System.out.println("1 * 2 = " + mult);
        System.out.println("2 / 1 = " + div);
        System.out.println("10 % 3 = " + mod);
    }

}
